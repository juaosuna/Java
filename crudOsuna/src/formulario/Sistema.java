    public void mostrarDatos() {
        String[] titulos = {"Tipo", "Documento", "Nombres", "Genero", "Direccion", "Celular"};
        String[] registros = new String[7];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        //String sentencia ="SELECT *"+"FROM aprendiz";
        String SQL = "select * from aprendiz1";

        try {
            Statement st = con.createStatement();
            var rs = st.executeQuery(SQL);

            while (rs.next()) {
                registros[0] = rs.getString("tipo_documento");
                registros[1] = rs.getString("num_documento");
                registros[2] = rs.getString("Nombres");
                registros[3] = rs.getString("Genero");
                registros[4] = rs.getString("Direccion");
                registros[5] = rs.getString("Celular");
                modelo.addRow(registros);
            }
            tablaAprendiz.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erorr de Busqueda" + e.getMessage());
        }
    }

