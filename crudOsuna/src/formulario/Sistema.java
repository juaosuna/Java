    public void insertarDatos() {
        try {
            //Datos que van a insertar en la base de datos
            String SQL = "insert into aprendiz (tipo_documento,num_documento,Nombres,Genero,Gireccion,Celular)values(?,?,?,?,?,?)";//insetar los datos

            java.sql.PreparedStatement pst = con.prepareStatement(SQL);
            int seleccionado = tipo_documento.getSelectedIndex();
            pst.setString(1, tipo_documento.getItemAt(seleccionado));

            pst.setInt(2, Integer.parseInt(num_documento.getText()));
            pst.setString(3, nombres.getText());

            int seleccionado1 = genero.getSelectedIndex();
            pst.setString(4, genero.getItemAt(seleccionado1));

            pst.setString(5, direccion.getText());

            pst.setDouble(6, Double.parseDouble(celular.getText()));

            pst.execute();
            JOptionPane.showMessageDialog(null, "Datos Registrados Correctamente.");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de Registro." + e.getMessage());
        }
    }

